    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        //creo variables para el nuevo producto con los datos ingresados
        String nombre = txtNombre.getText();
        double precio = Integer.parseInt(txtPrecio.getText());
        double stock = Double.parseDouble(txtPrecio.getText());
        String descripcion = txtDescripción.getText();
        //mando a la contorladora de la logica el usuario
        control.crearProducto(nombre, precio, stock,descripcion);
        mostrarMensaje("Producto creado correctamente", "info", "Creación exitosa");
        
    }//GEN-LAST:event_btnAgregarActionPerformed

